#!/bin/bash

# Останавливаем все запущенные экземпляры бота
pkill -f "python.*bot.py" || true

# Копируем файл .env.dev в .env
cp .env.dev .env

# Запускаем бота
python3 bot.py

# После завершения (Ctrl+C) восстанавливаем основной .env
